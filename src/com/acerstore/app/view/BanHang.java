    private void fillToTableSanPham() {
        modelSP = (DefaultTableModel) tblSanPham.getModel();
        modelSP.setRowCount(0);
        lstSP = daoSP.selectAll();
        for (SanPhamModel sp : lstSP) {
            modelSP.addRow(sp.toDataRow());
        }
    }

    private void fillToTableHoaDonCho() {
        modelHDC = (DefaultTableModel) tblHoaDonCho.getModel();
        modelHDC.setRowCount(0);
        lstHDC = daoHDC.selectAll();
        for (HoaDonCho hdc : lstHDC) {
            modelHDC.addRow(hdc.toDataRows());
        }
    }

