        public Integer countDB(){
            try {
                String sql = "select COUNT(*) from HoaDon";
                cn  = DBConnection.getConnection();
                st = cn.createStatement();
                rs = st.executeQuery(sql);
                while(rs.next()){
                    count = rs.getInt(1);
                }
                rs.close();
                st.close();
                cn.close();
            } catch (Exception e) {
                e.printStackTrace();
            }
            return count;
        }

