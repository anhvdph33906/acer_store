    private void showDetailSPCT(int index) {
        SanPhamChiTiets spct = spctdao.getAll().get(index);
        txtMaSP2.setText(spct.getMaSp());
        txtTenSP2.setText(spct.getTen());
        txtSoLuong.setText(spct.getSoLuong()+"");
        txtGia2.setText(spct.getGia()+"");
        txtManHinh4.setText(spct.getManHinh());
        cboRam4.setSelectedItem(spct.getRam());
        txtKichThuoc4.setText(spct.getKichThuoc());
    }

    private void fillToTableSPCT(List<SanPhamChiTiets> spd) {
        model = (DefaultTableModel) tblSanPhamChiTiet.getModel();
        model.setRowCount(0);
        for (SanPhamChiTiets s : spd) {
            model.addRow(new Object[]{
                s.getMaSp(),
                s.getTen(),
                s.getSoLuong(),
                s.getGia(),
                s.getManHinh(),
                s.getRam(),
                s.getKichThuoc(),
                s.getcPU(),
                s.getHeDieuHanh(),
                s.getMau(),
                s.isTrangThai() ? "Còn hàng" : "Hết hàng",
                s.getMoTa()
            });
        }
    }
