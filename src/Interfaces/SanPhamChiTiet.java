    private void fillToTableSPCT(List<SanPhamChiTiets> spct) {
        model = (DefaultTableModel) tblSanPhamChiTiet.getModel();
        model.setRowCount(0);
        if (spct != null) {
            for (SanPhamChiTiets s : spct) {
                model.addRow(new Object[]{
                    s.getMaSpct(),
                    s.getSp().getTenSP(),
                    s.getSoluong(),
                    s.getGia(),
                    s.getMh().getTenMh(),
                    s.getRa().getTenRam(),
                    s.getKt().getTenKT(),
                    s.getCp().getTenCPU(),
                    s.getHdh().getTenHDH(),
                    s.getOc().getTenOCung(),
                    s.getMa().getTenMau(),
                    s.getSp().isTrangThai() ? "Còn hàng" : "Hết hàng",
                    s.getSp().getMota()
                });
            }
        }
        cboImei.setSelectedItem(null);
    }

