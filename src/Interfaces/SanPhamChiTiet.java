    private void tblSanPhamChiTietMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamChiTietMouseClicked
        int index = tblSanPhamChiTiet.getSelectedRow();
        SanPhamChiTiets spct = spctdao.getAll().get(index);
        txtMaSP2.setText(String.valueOf(spct.getSanPhamChiTietId()));
        txtTenSP2.setText(spct.getTen());
        txtSoLuong.setText(String.valueOf(spct.getSoLuong()));
        txtGia2.setText(spct.getGia()+"");
        cboManHinh.setSelectedItem(tblSanPhamChiTiet.getValueAt(index, 4).toString());
        cboRam.setSelectedItem(tblSanPhamChiTiet.getValueAt(index, 5).toString());
        cboKichThuoc.setSelectedItem(tblSanPhamChiTiet.getValueAt(index, 6).toString());
        cboCPU.setSelectedItem(tblSanPhamChiTiet.getValueAt(index, 7).toString());
        cboHDH.setSelectedItem(tblSanPhamChiTiet.getValueAt(index, 8).toString());
        cboTrongLuong.setSelectedItem(tblSanPhamChiTiet.getValueAt(index, 9).toString());
        cboMau.setSelectedItem(tblSanPhamChiTiet.getValueAt(index, 10).toString());
        if(tblSanPhamChiTiet.getValueAt(index, 11).toString().equals("Còn hàng")){
            rdoConHang3.setSelected(true);
        }else{
            rdoHetHang3.setSelected(true);
        }
        txtMoTa.setText(tblSanPhamChiTiet.getValueAt(index, 12).toString());
        
    }//GEN-LAST:event_tblSanPhamChiTietMouseClicked

