    private void initCBOManHinh(ArrayList<ManHinh> list) {
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
            for (int i = 0; i < list.size(); i++) {
            model.addElement(list.get(i).getLoaiManHinh());
    }

        cboManHinh.setModel(model);
        }
