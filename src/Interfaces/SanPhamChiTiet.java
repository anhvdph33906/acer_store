    private void fillToTableSPCT(List<SanPhamChiTiets> spd) {
        model = (DefaultTableModel) tblSanPhamChiTiet.getModel();
        model.setRowCount(0);
//        List<SanPhamChiTiets> list = spctdao.getAll(sanPhamId);
        for (SanPhamChiTiets s : spd) {
            model.addRow(new Object[]{
                s.getSanPhamChiTietId(),
                s.getTen(),
                s.getSoLuong(),
                s.getGia(),
                s.getManHinh(),
                s.getRam(),
                s.getKichThuoc(),
                s.getcPU(),
                s.getHeDieuHanh(),
                s.getTrongLuong(),
                s.getMau(),
                s.isTrangThai() ? "Còn hàng" : "Hết hàng",
                s.getMoTa()
            });
        }
    }
    
