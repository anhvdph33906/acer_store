    public boolean checkSanPhamCT() {
        int soluong;
        double gia;
        if (txtSoLuong.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập số lượng");
            return false;
        } else {
            try {
                soluong = Integer.parseInt(txtSoLuong.getText());
                if (soluong <= 0) {
                    JOptionPane.showMessageDialog(this, "Số lượng phải lớn hơn 0");
                    return false;
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Sai định dạng số");
            }
        }

        if (txtGia2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập đơn giá");
            txtGia2.requestFocus();
            return false;
        } else {
            try {
                gia = Double.parseDouble(txtGia2.getText());
                if (gia <= 0) {
                    JOptionPane.showMessageDialog(this, "Đơn giá phải lớn hơn = 0");
                    txtGia2.requestFocus();
                    return false;
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Sai định dạng đơn giá");
                txtGia2.requestFocus();
                return false;
            }
        }
        return true;
    }

