    private void fillDM(List<DanhMucs> d) {
        dmtable = (DefaultTableModel) tblDM.getModel();
        dmtable.setRowCount(0);
        for(DanhMucs dm : d){
            dmtable.addRow(new Object[]{
                dm.getMaDm(),
                dm.getTenDm()
            });
        }
    }
    public DanhMucs readData(){
        String ten = txtDanhMuc.getText();
        int madm =0;
        return new DanhMucs(madm, ten);
    }
    public boolean checkTrong(){
        if(txtDanhMuc.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "không được để trống");
            txtDanhMuc.requestFocus();
            return false;
        }
        return true;
    }
