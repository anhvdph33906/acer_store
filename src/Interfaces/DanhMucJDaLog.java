    private void btnThemDmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemDmActionPerformed
        // TODO add your handling code here:
        if(checkTrong()){
            String danhmuc = txtDanhMuc.getText();
        if(txtDanhMuc.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "nhập danh mục");
        }
        else{
            dmdao.insertDM(danhmuc);
            JOptionPane.showMessageDialog(this, "thành công");
            fillDM(dmdao.getAllDM());
        }
        }
    }//GEN-LAST:event_btnThemDmActionPerformed

    private void tblDMMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDMMouseClicked
        // TODO add your handling code here:
        int index = tblDM.getSelectedRow();
        DanhMucs dms = dmdao.getAllDM().get(index);
        txtDanhMuc.setText(dms.getTenDm());
        btnSua.setEnabled(true);
        btnXoa.setEnabled(true);
    }//GEN-LAST:event_tblDMMouseClicked

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int index = tblDM.getSelectedRow();
        String ma = String.valueOf(tblDM.getValueAt(index, 0));
        if(dmdao.deleteDM(ma)>0){
            JOptionPane.showMessageDialog(this, "xóa thành công");
            fillDM(dmdao.getAllDM());
        }else{
            JOptionPane.showMessageDialog(this, "xóa thất bại");
        }
        txtDanhMuc.setText("");
        btnSua.setEnabled(false);
        btnXoa.setEnabled(false);
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        if(checkTrong()){
            int index = tblDM.getSelectedRow();
        DanhMucs dms = readData();
        String ma = String.valueOf(tblDM.getValueAt(index, 0));
        if(dmdao.updateDM(dms, ma)>0){
            JOptionPane.showMessageDialog(this, "sửa thành công");
            fillDM(dmdao.getAllDM());
        }
        else{
            JOptionPane.showMessageDialog(this, "sửa thất bại");
        }
        txtDanhMuc.setText("");
        btnSua.setEnabled(false);
        btnXoa.setEnabled(false);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

