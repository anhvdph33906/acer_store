    private void fillToTableThongSo(List<ThongSo> ts) {
        mo = (DefaultTableModel) tblThongSo.getModel();
        mo.setRowCount(0);
        for(ThongSo t : ts){
            mo.addRow(new Object[]{
                t.getManHinh(),
                t.getRam(),
                t.getKichThuoc(),
                t.getcPU(),
                t.getHeDH(),
                t.getTrongLuong(),
                t.getMau()
            });
        }
    }

    private void showDetailThongSo(int index ) {
        ThongSo t = spctdao.getAllTS().get(index);
        txtManHinha.setText(t.getManHinh());
        cboRam4.setSelectedItem(t.getRam());
        txtKichThuoca.setText(t.getKichThuoc());
        cboCpu.setSelectedItem(t.getcPU());
        txtHeDieuHanha.setText(t.getHeDH());
        txtTrongLuong.setText(t.getTrongLuong());
        cboMau4.setSelectedItem(t.getMau());
        tblThongSo.setRowSelectionInterval(index, index);
    }

    private ThongSo readDataTS() {
        String mh = txtManHinha.getText();
        String ram = String.valueOf(cboRam4.getSelectedItem());
        String kt = txtKichThuoca.getText();
        String cpu = cboCpu.getSelectedItem().toString();
        String hdh = txtHeDieuHanha.getText();
        String tl = txtTrongLuong.getText();
        String m = cboMau4.getSelectedItem().toString();
        return new ThongSo(hdh, ram, hdh, cpu, hdh, tl, m);
    }

    private boolean checkDataTS() {
        if(txtManHinha.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "empty");
            txtManHinha.requestFocus();
            return false;
        }
        if(txtKichThuoca.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "empty");
            txtKichThuoca.requestFocus();
            return false;
        }
        if(txtHeDieuHanha.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "empty");
            txtHeDieuHanha.requestFocus();
            return false;
        }
        if(txtTrongLuong.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "empty");
            txtTrongLuong.requestFocus();
            return false;
        }
        return true;
    }
