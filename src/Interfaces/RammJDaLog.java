    private void filltoTableRam(List<Ram> ram) {
        tblmodel = (DefaultTableModel) tblRam.getModel();
        tblmodel.setRowCount(0);
        for (Ram ra : ram) {
            tblmodel.addRow(new Object[]{
                ra.getIdRam(),
                ra.getTenRam(),
                ra.isTtRam() ? "Hoạt động" : "Không hoạt động"
            });
        }
    }

    private Ram readDataRam() {
        int id = 0;
        String ten = txtRam.getText();
        boolean tt;
        if (tt = rdoHD.isSelected()) {
            tt = true;
        } else {
            tt = false;
        }
        return new Ram(id, ten, tt);
    }

    private void addRam() {
        if (checkDataRam()) {
            Ram ra = readDataRam();
            if (spcdao.AddRam(ra) > 0) {
                JOptionPane.showMessageDialog(this, "add successful");
                filltoTableRam(spcdao.getRam());
            } else {
                JOptionPane.showMessageDialog(this, "add failed");
            }
        }
    }

    private void deleteRam() {
        int index = tblRam.getSelectedRow();
        int id = Integer.parseInt(tblRam.getValueAt(index, 0).toString());
        if (spcdao.deleteRam(id) > 0) {
            JOptionPane.showMessageDialog(this, "delete successful");
            filltoTableRam(spcdao.getRam());
        } else {
            JOptionPane.showMessageDialog(this, "delete failed");
        }
        txtRam.setText("");
        rdoHD.isSelected();
        btnDelete.setEnabled(false);
    }

    private void updateRam() {
        if (checkDataRam()) {
            Ram ram = readDataRam();
            int index = tblRam.getSelectedRow();
            int id = Integer.parseInt(tblRam.getValueAt(index, 0).toString());
            if (spcdao.updateRam(ram, id) > 0) {
                JOptionPane.showMessageDialog(this, "update successful");
                filltoTableRam(spcdao.getRam());
            } else {
                JOptionPane.showMessageDialog(this, "update failed");
            }
            txtRam.setText("");
            rdoHD.isSelected();
            btnUpdate.setEnabled(false);
        }
    }

    private void showDetailRam(int index) {
        Ram ra = spcdao.getRam().get(index);
        txtRam.setText(ra.getTenRam());
        rdoHD.setSelected(ra.isTtRam());
        tblRam.setRowSelectionInterval(index, index);
    }

    private boolean checkDataRam() {
        if (txtRam.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "empty");
            txtRam.requestFocus();
            return false;
        }
        return true;
    }
